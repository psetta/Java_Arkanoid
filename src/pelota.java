public class pelota {
    public int x;
    public int y;
    public float velo;
    public float veloX;
    public float veloY;
    public int ladoPelota;
   
    public pelota(int x, int y, float velo, float veloX, float veloY, int ladoPelota) {
        this.x = x;
        this.y = y;
        this.velo = velo;
        this.veloX = veloX;
        this.veloY = veloY;
        this.ladoPelota= ladoPelota;
    }
}
